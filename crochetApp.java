    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // To add the products from the form into the linkedlist
        int productId = Integer.parseInt(txtProdid.getText());
        String productName = txtProdName.getText();
        String category = (String) boxCategory.getSelectedItem();
        String yarnType = (String) boxYarnType.getSelectedItem();
        boolean repairable = checkRepair.isSelected();
        String description = txtDescription.getText();
        float price = Float.parseFloat(txtPrice.getText());
        int stock = (int) spinStock.getValue();
        
        SimpleDateFormat formatDate = new SimpleDateFormat("yyyy-MM-dd");
        String currentDate = formatDate.format(new Date());
        
        crochetModel product = new crochetModel(productId, productName, category, yarnType, repairable, description, price, stock, currentDate);
        addProduct(product);
    }//GEN-LAST:event_btnAddActionPerformed

