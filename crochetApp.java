    // Method to simulate loading progress
    private void forProgressBar() {
        javax.swing.SwingWorker<Void, Integer> worker = new javax.swing.SwingWorker<>() {
            @Override
            protected Void doInBackground() throws Exception {
                for (int i = 0; i <= 100; i++) {
                    Thread.sleep(30); // Simulated delay for progress bar
                    publish(i);
                }
                return null;
            }

            @Override
            protected void process(java.util.List<Integer> chunks) {
                int progress = chunks.get(chunks.size() - 1);
                progressBar.setValue(progress);
            }

            @Override
            protected void done() {
                loadScreen("LoginScreen"); // Switch to login screen
            }
        };
        worker.execute();
    }
    
    // Method to add the products data into the table
    private void addProduct(crochetModel product) {
        productList.add(product);
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        model.addRow(new Object[]{
            product.getProdId(), product.getProdName(), product.getCategory(),product.getYarnType(), product.isRepairable(),product.getDescription(),  product.getPrice(), product.getStock(), product.getDateAdded(),
        });
    }
    
