    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Get the input from log-in page
        String userName = txtUserName.getText();
        String password = new String(txtPassword.getPassword());

        // Check if fields are empty
        if (userName.isEmpty() || password.isEmpty()) {
            lblError.setText("Please enter your username and password!");
        } // Check username and password
        else if (!userName.equals("admin") || !password.equals("admin")) {
            lblError.setText("Username and password don't match!!!");
        } // Proceed to load the main screen after datas are validated
        else {
            lblError.setText(""); // Clear previous error messages
            loadScreen("MainScreen"); // Load main screen
        }
    }//GEN-LAST:event_btnLoginActionPerformed

